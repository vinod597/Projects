package com.pbsi.fuelfilling.json;

public class FuelFiling3Json {
	private String date;
	private Float previousReading;
	private Float presentReading;
	private Float distanceTraveled;
	private Float fuelCost;
	private Float fuelFilled;
	private Float billAmount;
	private Float payments;
	private Float amountReturned;
	private Float creditLimit;
	private Float creditAvailed;
	private Float creditAvailable;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public Float getPreviousReading() {
		return previousReading;
	}

	public void setPreviousReading(Float previousReading) {
		this.previousReading = previousReading;
	}

	public Float getPresentReading() {
		return presentReading;
	}

	public void setPresentReading(Float presentReading) {
		this.presentReading = presentReading;
	}

	public Float getDistanceTraveled() {
		return distanceTraveled;
	}

	public void setDistanceTraveled(Float distanceTraveled) {
		this.distanceTraveled = distanceTraveled;
	}

	public Float getFuelCost() {
		return fuelCost;
	}

	public void setFuelCost(Float fuelCost) {
		this.fuelCost = fuelCost;
	}

	public Float getFuelFilled() {
		return fuelFilled;
	}

	public void setFuelFilled(Float fuelFilled) {
		this.fuelFilled = fuelFilled;
	}

	public Float getBillAmount() {
		return billAmount;
	}

	public void setBillAmount(Float billAmount) {
		this.billAmount = billAmount;
	}

	public Float getPayments() {
		return payments;
	}

	public void setPayments(Float payments) {
		this.payments = payments;
	}

	public Float getAmountReturned() {
		return amountReturned;
	}

	public void setAmountReturned(Float amountReturned) {
		this.amountReturned = amountReturned;
	}

	public Float getCreditLimit() {
		return creditLimit;
	}

	public void setCreditLimit(Float creditLimit) {
		this.creditLimit = creditLimit;
	}

	public Float getCreditAvailed() {
		return creditAvailed;
	}

	public void setCreditAvailed(Float creditAvailed) {
		this.creditAvailed = creditAvailed;
	}

	public Float getCreditAvailable() {
		return creditAvailable;
	}

	public void setCreditAvailable(Float creditAvailable) {
		this.creditAvailable = creditAvailable;
	}

}
